package com.github.dzirbel.battlecompanion.core

import kotlin.random.Random

/**
 * Represents the state of the combat board at a specific time, which simply contains the
 *  [attackers] and [defenders].
 * Note that since [Army] is immutable, [Board] is as well.
 * TODO submarine submerging
 * TODO attackers retreating
 * TODO listener system
 * TODO validity check (maybe only used in tests?) that land units aren't against sea units, etc
 * TODO industrial complex bombing?
 */
data class Board(
    val attackers: Army,
    val defenders: Army
) {

    /**
     * Gets the outcome of the combat on this [Board], or null if the battle is not decided.
     */
    fun getOutcome(): Outcome? {
        return when {
            attackers.units.isEmpty() && defenders.units.isEmpty() -> Outcome.Tie
            attackers.units.isEmpty() && defenders.units.isNotEmpty() ->
                Outcome.DefenderWon(defenders)
            attackers.units.isNotEmpty() && defenders.units.isEmpty() ->
                Outcome.AttackerWon(attackers)
            else -> null
        }
    }

    /**
     * Conducts a round of combat (including both opening and regular fire) based on the rolls generated by the given
     *  [Random] and returns a [Board] with the result.
     */
    fun roll(rand: Random): Board {
        val attackerOpeningFire = attackers.rollHits(
            rand = rand,
            enemies = defenders,
            isAttacking = true,
            isOpeningFire = true
        )
        val defenderOpeningFire = defenders.rollHits(
            rand = rand,
            enemies = attackers,
            isAttacking = false,
            isOpeningFire = true
        )

        val remainingAttackers = attackers.takeHits(defenderOpeningFire)
        val remainingDefenders = defenders.takeHits(attackerOpeningFire)

        val attackerHits = remainingAttackers.rollHits(
            rand = rand,
            enemies = remainingDefenders,
            isAttacking = true,
            isOpeningFire = false
        )
        val defenderHits = remainingDefenders.rollHits(
            rand = rand,
            enemies = remainingAttackers,
            isAttacking = false,
            isOpeningFire = false
        )

        return Board(
            attackers = remainingAttackers.takeHits(defenderHits),
            defenders = remainingDefenders.takeHits(attackerHits)
        )
    }

    /**
     * Returns a copy of this [Board] without [UnitType]s that should only fire during the first round of the battle,
     *  i.e. bombarding battleships and defending antiaircraft guns.
     */
    fun withoutFirstRoundOnlyUnits(): Board {
        return Board(
            attackers = attackers.withoutFirstRoundOnlyUnits(),
            defenders = defenders.withoutFirstRoundOnlyUnits()
        )
    }
}
